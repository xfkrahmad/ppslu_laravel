                                <p> Tahun 1933 Di Banyumas didirikan Algenine Zorg Voor Inlander Beheftegen disingkat
                                    AZIB
                                    diketuai Asisten Wedana Banyumas, dan pelindung Ny. Bupati Banyumas. Perkumpulan
                                    tersebut didirikan karena adanya anjuran dari Ny. Gunernur De Yong agar orang-orang
                                    yang
                                    kehidupannya sangat miskin supaya mendapat perhatian dan ditampung dalam suatu
                                    asrama.
                                    Kegiatan perkumpulan mengambil tempat di gedung Jayengan, yang letaknya disebelah
                                    selatan kota Banyumas dan telah menampung beberapa orang keluarga miskin yang setiap
                                    harinya diberi pendidikan kerajinan tangan, menganyam tikar, dan lain-lain.
                                    Sedangkan
                                    makannya, ditanggung orang-orang dermawan dan usaha lainnya.</p>
                                <p> Tahun 1938 di Banyumas direncanakan akan didirikan suatu perumahan bagi
                                    Zwaksinnegen,
                                    dan akan didirikan di tempat penampungan AZIB yaitu desa Jayengan. Oleh karena itu,
                                    AZIB
                                    terpaksa harus dipindahkan penampungannya ke Kota Banyumas di bekas pasar yang
                                    disebut
                                    Pasar Praja. Karena usaha dan kemajuan-kemajuan yang dicapainya, maka AZIB kecuali
                                    mendapat bantuan dari masyarakat juga mendapat bantuan dari Regenshap (Pemerintah).
                                </p>
                                <p> Sekitar tahun 1942, terjadilah pergantian dari zaman Belanda ke zaman Jepang yang
                                    mengakibatkan berbagai kesulitan. Namun atas ketangguhan dari para pengurusnya, yang
                                    saat itu diketuai oleh Asisten Wedana dan lain-lainnya, maka rumah perawat AZIB
                                    didirikan kembali dengan nama Rumah Sengsara.</p>
                                <p> Tahun 1947, dengan adanya Agresi Belanda Rumah Sengsara menjadi kacau balau dan
                                    berhenti. Namun berkat keuletan dari para pengurusnya yang masih tetap sama, maka
                                    setelah Agresi Belanda rumah perawatan tersebut dapat didirikan kembali dengan nama
                                    tetap yaitu Rumah Sengsara. Sedangkan biaya perawatan beralih menjadi tanggung jawab
                                    Dinas Sosial.</p>
                                <p> Tahun 1949, rumah sengsara diganti namanya dengan Panti “Pangrukti Wilopo” yang
                                    masih
                                    menampung keluarga miskin atau orang-orang gelandangan di mana orang tua dan
                                    anak-anak
                                    masih dicampur dalam satu asrama.</p>
                                <p> Pada tanggal 1 Januari 1953, Panti “Pangrukti Wilopo” diganti menjadi “Pamardi
                                    Yoga”.
                                    Sejak saat itu mengkhususkan merawat anak-anak terlantar. Sedangkan para penghuni
                                    orang-orang dewasa telah disalurkan ke panti perawatan khusus orang dewasa. Dan
                                    sejak
                                    itu pula biaya perawatan dan lain-lainnya menjadi tanggung jawab sepenuhnya dari
                                    pemerintah dalam hal ini Depertemen Sosial RI.</p>

                                <p> Tahun 1955, beberapa pekerjaan sosial dan Depertemen Sosial oleh Pemerintah pusat
                                    diserahkan kepada Pemerintah Daerah TK I antara lain termasuk Panti Asuhan, maka
                                    sejak
                                    itu semua biaya perawatan dan lain-lain menjadi tanggung jawab Pemerintah Daerah TK
                                    I.
                                    Tepat pada tanggal 16 Desember 1955, nama Panti “Pamardi Yoga” diganti dengan nama
                                    Panti
                                    Asuhan “Budhi Sakti” Banyumas.
                                    Berdasarkan Peraturan Daerah Propinsi Jawa Tengah No. 1 Tahun 2002 Panti Asuhan
                                    “Budhi
                                    Sakti” di ambil alih oleh Propinsi, dan sejak itu pula menjadi tanggung jawab Dinas
                                    Kesejahteraan Sosial Propinsi Jawa Tengah.
                                    Berdasarkan Peraturan Gubernur Jawa Tengah No. 50 Tahun 2008 tentang Pembentukan SOT
                                    Baru, Dinas Sosial Provinsi Jawa Tengah. Sebagai salah satu Unit Pelaksana Teknis
                                    (UPT)
                                    yang menangani masalah anak terlantar, Panti Asuhan “Budhi Sakti” memiliki Satker
                                    Panti
                                    Asuhan “Mardi Yuwono” di Wonosobo</p>
                                <p> Sesuai Peraturan Gubernur Jawa Tengah No. 111 Tahun 2010 Tanggal 1 November 2010
                                    tentang
                                    Organisasi dan Tata Kerja Unit Pelaksana Teknis Pada Dinas Sosial Provinsi Jawa
                                    Tengah,
                                    Panti Asuhan “Budhi Sakti” Banyumas ditingkatkan fungsinya menjadi Balai
                                    Rehabilitasi
                                    Sosial yang memiliki Unit Rehabilitasi Sosial “ Pamardi Raharjo” Banjarnegara</p>
                                <p> Sesuai Peraturan Gubernur Jawa Tengah No. 53 Tahun 2013 Tanggal 22 Agustus 2013
                                    tentang
                                    Organisasi dan Tata Kerja Unit Pelaksana Teknis Pada Dinas Sosial Provinsi Jawa
                                    Tengah,
                                    Balai Pelayanan Sosial Asuhan Anak “Budhi Sakti” Banyumas yang memiliki Unit
                                    Rehabilitasi Sosial PGOT “Pamardi Raharjo” Banjarnegara dengan daya tampung 50 Orang
                                    PGOT.</p>
                                <p> Sesuai Peraturan Gubernur Jawa Tengah No. 109 Tahun 2016 tentang Organisasi dan tata
                                    kerja Unit Pelaksana Teknis Dinas Sosial Provinsi Jawa Tengah, Balai Pelayanan
                                    Sosial
                                    Asuhan Anak “Budhi Sakti” Banyumas yang memiliki Unit Rehabilitasi Sosial PGOT “
                                    Pamardi
                                    Raharjo” Banjarnegara dengan daya tampung 50 Orang PGOT ganti nama dan alih fungsi
                                    menjadi Panti Pelayanan Sosial Lanjut Usia “Sudagaran” Banyumas melayani Penerima
                                    Manfaat Lanjut Usia sebanyak 100 Orang dan memiliki Rumah Persinggahan Sosial
                                    “Pamardi
                                    Raharjo” Banjarnegara melayani Penerima Manfaat Penyandang Masalah Kesejahteraan
                                    Sosial
                                    segala umur sebanyak 50 Orang.</p>
                                <p> Berdasarkan Peraturan Gubernur Jawa Tengah Nomor 31 Tahun 2018 tentang Organisasi
                                    dan
                                    Tata Kerja Unit Pelaksana Teknis pada Dinas Sosial Provinsi Jawa Tengah, Panti
                                    Pelayanan
                                    Sosial Lanjut Usia “Sudagaran” Banyumas menjadi Panti Tipe B yang dipimpin oleh
                                    seorang
                                    Pejabat Struktural Eselon IV dan membawahi satu Sub Bagian Tata Usaha. Adapun
                                    seksi-seksi dilebur menjadi satu dan dipimpin langsung oleh Kepala Panti. Sesuai
                                    dengan
                                    Pergub ini, Rumah Persinggahan Sosial “Pamardi Raharjo” Banjarnegara beralih fungsi
                                    menjadi Rumah Pelayanan Sosial PMKS “Pamardi Raharjo” Banjarnegara yang melayani
                                    semua
                                    jenis PMKS. Berdasarkan Pergub ini pula, Rumah Pelayanan Sosial PMKS “Pamardi
                                    Raharjo”
                                    Banjarnegara berpisah dari Panti Pelayanan Sosial Lanjut Usia “Sudagaran” Banyumas
                                    dan
                                    menjadi unit dari Panti Pelayanan Sosial Lanjut Usia “Dewanata” Cilacap.</p>

